from backend.SudokuTechniques.SudokuHints import SudokuHints
import unittest  

class Test_Hints(unittest.TestCase):

    grid = [["-","9","2","-","-","5","7","6","-"],
            ["6","-","7","4","-","3","1","-","-"],
            ["1","3","8","-","6","7","4","-","2"],
            ["-","7","-","2","3","-","6","1","5"],
            ["-","1","-","5","-","6","2","4","7"],
            ["2","6","5","-","4","-","8","3","9"],
            ["5","-","-","1","9","2","-","7","4"],
            ["9","-","3","-","7","-","5","2","1"],
            ["7","-","1","-","-","4","9","-","6"]]

    def test_constructor(self):
        sudoku = SudokuHints(self.grid)
        self.assertEqual(sudoku.grid, [[0,9,2,0,0,5,7,6,0],
                                       [6,0,7,4,0,3,1,0,0],
                                       [1,3,8,0,6,7,4,0,2],
                                       [0,7,0,2,3,0,6,1,5],
                                       [0,1,0,5,0,6,2,4,7],
                                       [2,6,5,0,4,0,8,3,9],
                                       [5,0,0,1,9,2,0,7,4],
                                       [9,0,3,0,7,0,5,2,1],
                                       [7,0,1,0,0,4,9,0,6]])
